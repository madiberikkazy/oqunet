echo "✅ Checking HTML updates..."
grep -q "manifest.json" index.html && echo "   ✓ Manifest link added" || echo "   ✗ Manifest link missing"
grep -q "serviceWorker" index.html && echo "   ✓ Service Worker registration" || echo "   ✗ SW registration missing"
